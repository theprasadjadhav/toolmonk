// ─── Helper Functions ─────────────────────────────────────────────────────────

export function getToolsByCategory(category: ToolCategory): ToolMeta[] {
  return TOOLS.filter((t) => t.category === category);
}

export function getToolsBySubcategory(
  category: ToolCategory,
  subcategory: string
): ToolMeta[] {
  return TOOLS.filter(
    (t) => t.category === category && t.subcategory === subcategory
  );
}

export function getFlatToolsByCategory(category: ToolCategory): ToolMeta[] {
  return TOOLS.filter((t) => t.category === category && !t.subcategory);
}

export function getToolBySlug(slug: string): ToolMeta | undefined {
  return TOOLS.find((t) => t.slug === slug);
}

export function getToolByPath(path: string): ToolMeta | undefined {
  return TOOLS.find((t) => t.path === path);
}

export function getFeaturedTools(limit = 16): ToolMeta[] {
  return TOOLS.filter((t) => t.featured).slice(0, limit);
}

export function getRelatedTools(slugs: string[]): ToolMeta[] {
  return slugs
    .map((s) => TOOLS.find((t) => t.slug === s))
    .filter((t): t is ToolMeta => t !== undefined);
}

export function getCategoryBySlug(
  slug: string
): CategoryMeta | undefined {
  return CATEGORIES.find((c) => c.slug === slug);
}

export function getSubcategoriesByCategory(
  category: ToolCategory
): SubcategoryMeta[] {
  return SUBCATEGORIES.filter((s) => s.category === category);
}

/** Returns params for generateStaticParams at the /[category] level */
export function getCategoryStaticParams() {
  return CATEGORIES.map((c) => ({ category: c.slug }));
}

/**
 * Returns params for generateStaticParams at the /[category]/[segment] level.
 * Covers both subcategory pages and flat tool pages.
 */
export function getSegmentStaticParams() {
  const subcategoryParams = SUBCATEGORIES.map((s) => ({
    category: s.category,
    segment: s.slug,
  }));
  const toolParams = TOOLS.filter((t) => !t.subcategory).map((t) => ({
    category: t.category,
    segment: t.slug,
  }));
  return [...subcategoryParams, ...toolParams];
}

/**
 * Returns params for generateStaticParams at the /[category]/[segment]/[tool] level.
 * Covers all 3-level nested tool pages.
 */
export function getToolStaticParams() {
  return TOOLS.filter((t) => t.subcategory !== undefined).map((t) => ({
    category: t.category,
    segment: t.subcategory!,
    tool: t.slug,
  }));
}

/** Number of unique tools (excludes cross-category alias entries) */
export function getUniqueToolCount(): number {
  return TOOLS.filter((t) => !t.aliasOf).length;
}

/** Returns all paths for the sitemap */
export function getAllPaths(): string[] {
  return [
    "/",
    ...CATEGORIES.map((c) => c.path),
    ...SUBCATEGORIES.map((s) => s.path),
    ...TOOLS.map((t) => t.path),
  ];
}
